OUT_TXT = "RegTorg_offers_descriptions_200.txt"

def load_offers(xml_path):
    text = Path(xml_path).read_text(encoding="utf-8")
    root = ET.fromstring(text)
    # маппинг categoryId -> название рубрики
    cat_map = {c.get("id"): (c.text or "").strip()
               for c in root.findall(".//category")}
    offers = []
    for off in root.findall(".//offer"):
        oid = off.get("id") or ""
        name = (off.findtext("name") or "").strip()
        catid = off.findtext("categoryId") or ""
        category = cat_map.get(catid, "").strip()
        offers.append({"id": oid, "name": name, "category": category})
    return offers

def base_clean(name: str) -> str:
    # убираем технические хвосты типа (YML5024)
    return re.sub(r"\(YML\d+\)", "", name).strip()

