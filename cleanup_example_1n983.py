import re

raw = """
1Н983, 1А983, 1М983, РТ983, СА98, СА983, 1Н98, 1Н983.90.153, 1Н983.90.151, 1Н983.90.000СБ, 1М983, 1А983, 1Н983, СА983, РТ983, РТ783, РТ783Ф3, 1Н983.44.000СБ
1Н983.44.000СБ, 1Н983, 1М983, 1А983, РТ983, СА983, РТ772Ф3, 1Н983.60.110, РТ983., Э11М, РТ783, 1Н983.20.124, 1Н983.90.163, РТ783Ф3
1Н983, 1А983, 1М983, РТ983, СА983, 16К20, 16К25, 1К62, 16Р25П, СА630, 1А660, 16Б16, 1К62.02.320СБ, 16В20.20.900, 16В20, 16Р25 16К20.020.074, 16К20
"""

# Разбиваем на "слова" по пробелам и запятым/переводам строк
tokens = re.split(r"[,\s]+", raw)
tokens = [t for t in tokens if t]

codes = []

for t in tokens:
    t = t.strip(" ,;")

    # убираем одиночную точку на конце (РТ983. -> РТ983)
    if t.endswith(".") and "." not in t[:-1]:
        t = t[:-1]

    # фильтр: должна быть хотя бы одна буква и одна цифра
    has_letter = bool(re.search(r"[A-Za-zА-Яа-яЁё]", t))
    has_digit = bool(re.search(r"\d", t))
    if not (has_letter and has_digit):
        continue

    # убираем очевидные мусорные коротыши, например СА98, 1Н98 (если хочешь их оставить — сними этот фильтр)
    if len(t) < 5 and not "." in t:
        continue

    codes.append(t)

# убираем дубли, сохраняем порядок
seen = set()
unique = []
for c in codes:
    if c not in seen:
        seen.add(c)
        unique.append(c)

result = ", ".join(unique)
print(result)