def update_line(line: str) -> str:
    if ";;" not in line:
        return line

    left, right = line.split(";;", 1)

    # ищем SKU во второй части
    sku_key = None
    for sku in CHPU_DATA.keys():
        if sku in right:
            sku_key = sku
            break
    if not sku_key:
        return line

    data = CHPU_DATA[sku_key]

    parts = right.split(";")
    while len(parts) < 15:
        parts.append("")

    # 0 SKU;1 Brand;2 SKU;3 пусто;4 Category;5 Category2;6 Title;7 Description;8 Text;...
    parts[7] = data["desc"]
    parts[8] = data["text"]

    new_right = ";".join(parts)
    return f"{left};;{new_right}"


def main():
    if not TOPCSV.exists():
        print(f"Файл не найден: {TOPCSV}")
        return

    lines = TOPCSV.read_text(encoding="utf-8-sig").splitlines()
    new_lines = []
    updated = 0

    for line in lines:
        new_line = update_line(line)
        if new_line != line:
            updated += 1
        new_lines.append(new_line)

    out_path = TOPCSV.with_name(TOPCSV.stem + "-CHPU-TEXT.csv")
    out_path.write_text("\n".join(new_lines), encoding="utf-8-sig")
    print(f"Обновлено строк: {updated}")
    print(f"Сохранено: {out_path}")


if __name__ == "__main__":
    main()
