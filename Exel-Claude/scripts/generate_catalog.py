def is_marker_only(text) -> bool:
    if pd.isna(text) or str(text).strip() == "":
        return True
    cleaned = re.sub(r'__[A-Za-zА-ЯЁа-яё0-9.\-]+__', '', str(text)).strip()
    return cleaned == ""

def has_servicesblock(text) -> bool:
    return isinstance(text, str) and "td-rss.ru" in text

def strip_shiphers(text: str) -> str:
    return re.sub(r'__[A-Za-zА-ЯЁа-яё0-9.\-]+__', '', text).strip()

def classify_part(name, category, code, source_hint) -> str:
    combined = " ".join([str(x).lower() for x in [name, category, source_hint]])
    unit_desc = "деталь металлорежущего станка"
    material  = "конструкционная или легированная сталь с термообработкой"
    kinematic = "входит в кинематическую цепь привода станка"
    for pattern, u, m, k in KEYWORD_MAP:
        if re.search(pattern, combined, re.IGNORECASE):
            unit_desc, material, kinematic = u, m, k
            break
    model_m = re.search(
        r'\b(1[МмMm][0-9][0-9А-ЯЁA-Z]?|16[КкKkРрRrМмMmБбBbВвVv][0-9]+[А-ЯЁA-Za-z0-9]*'
        r'|1[Кк][0-9]+|1[Нн][0-9]+|6[РрMмTт][0-9]+[А-ЯЁa-z]*'
        r'|РТ[0-9]+[А-ЯЁa-z0-9]*|ДИП[0-9]+)\b',
        str(name), re.IGNORECASE)
    model_str = f" токарного станка {model_m.group(0)}" if model_m else ""
    params = re.findall(r'(?:z|m|модуль)\s*[=\-]?\s*[0-9]+', str(name), re.IGNORECASE)
    params_str = f" Параметры: {', '.join(params)}." if params else ""
    code_s = str(code).strip() if not pd.isna(code) else ""
    code_str = f" Артикул: {code_s}." if code_s and not code_s.startswith("__") else ""
    desc = (f"{str(name).strip()} — {unit_desc}{model_str}. "
            f"{kinematic.capitalize()}.{params_str} "
            f"Изготавливается из {material}.{code_str}").strip()
    # Grammar fix
    desc = re.sub(r'\bиз (сталь|бронза|чугун|алюминий)\b',
        lambda m: "из " + (m.group(1)[:-1]+"и" if m.group(1)[-1] in "ьа" else m.group(1)), desc)
    desc = strip_shiphers(desc)
    desc = re.sub(r'  +', ' ', desc)
    return desc + SERVICESBLOCK


def main():
    for enc in ("utf-8-sig", "utf-8", "cp1251", "latin-1"):
        try:
            df = pd.read_csv(INPUT_PATH, encoding=enc, low_memory=False, dtype=str)
            print(f"Loaded: encoding={enc}, shape={df.shape}")
            break
        except Exception as e:
            print(f"  {enc}: {e}")
    else:
        sys.exit("ERROR: cannot read file")

    for col in ["description_draft","sourceflag","new_description_top","quality_flag","edit_flag"]:
        if col not in df.columns:
            df[col] = ""

    mask = df["new_description_top"].apply(is_marker_only)
    print(f"Need generation: {mask.sum()} / {len(df)}")

    for idx in df.index[mask]:
        df.at[idx, "new_description_top"] = classify_part(
            df.at[idx, "name"] if "name" in df.columns else "",
            df.at[idx, "category"] if "category" in df.columns else "",
            df.at[idx, "code"] if "code" in df.columns else "",
            df.at[idx, "source_hint"] if "source_hint" in df.columns else "")
        df.at[idx, "sourceflag"]   = "generated"
        df.at[idx, "quality_flag"] = "auto"
        df.at[idx, "edit_flag"]    = "light_edit"

    with_sb = df["new_description_top"].apply(has_servicesblock).sum()
    print(f"With SERVICESBLOCK: {with_sb}")
    df.to_csv(OUTPUT_PATH, index=False, encoding="utf-8-sig")
    print(f"Saved: {OUTPUT_PATH}")

if __name__ == "__main__":
    main()