def extract_article(name):
    """Последняя часть названия похожая на артикул"""
    # ищем паттерн типа 1М63Б.70.218 или 16М30Ф3.57.000
    matches = re.findall(r'[\dА-ЯA-Z][\w\-\.\/]+', name)
    # берём последний подходящий (обычно это артикул)
    for m in reversed(matches):
        if '.' in m and len(m) > 4:
            return m
    return matches[-1] if matches else ""

def extract_machine_model(name, article):
    """Извлекаем модель станка из названия или артикула"""
    # Явно указанные в названии модели (после слов "для станка", "станков", "станка")
    m = re.search(r'(?:для\s+станк[аов]+\s+|станк[аов]+\s+)([\w,\s\-]+?)(?:\s*$|\s*,|\s+\()', name, re.IGNORECASE)
    if m:
        return m.group(1).strip()

    # Из артикула — часть до первой точки
    if article:
        prefix = article.split('.')[0]
        # убираем числовые суффиксы типа .20.274
        if re.match(r'^[\dА-ЯA-Z\-]+$', prefix) and len(prefix) >= 3:
            return prefix
    return ""

