def read_product_list():
    products = []
    with open(SOURCE, 'r', encoding='utf-8') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            # Формат может быть "1\tНазвание", "1 Название" или просто "Название"
            m = re.match(r'^\d+[\t\s]+(.+)$', line)
            if m:
                products.append(m.group(1).strip())
            else:
                # Строка без номера — берём как есть
                products.append(line)
    return products

