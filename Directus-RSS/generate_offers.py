def get_compatible(name, machine):
    """Формируем список совместимых станков из названия"""
    compat = []
    # Явно перечисленные в названии через запятые модели
    # Ищем упоминания моделей типа 1М63, 1Н983, ДИП500 и т.д.
    models = re.findall(r'\b(?:1[А-ЯA-Z]\d{2}[А-ЯA-Z\-]*[\w\-]*|ДИП\d+|РТ\d+[\w]*|6[А-ЯA-Z]\d{2}[\w\-]*|16[А-ЯA-Z\d][\w\-]*|FU\d+|FSS\s*\d+|2[А-ЯA-Z\d][\w\-]*|53А\d+|RAFAMET[\w\s]*)', name)
    seen = set()
    for m in models:
        m = m.strip()
        if m and m not in seen:
            seen.add(m)
            compat.append(m)
    if machine and machine not in seen:
        compat.insert(0, machine)
    if not compat and machine:
        compat = [machine]
    return compat

