def translit(text):
    res = ""
    for ch in text:
        res += TRANSLIT.get(ch, ch)
    # убираем лишние символы кроме букв, цифр, точек, дефисов, подчёркиваний
    res = re.sub(r'[^\w.\-]', '_', res)
    res = re.sub(r'_+', '_', res)
    return res.strip('_')

